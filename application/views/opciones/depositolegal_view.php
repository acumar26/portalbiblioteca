                <p>El depósito legal es el cumplimiento de la Ley N° 26905 modificada por Ley N° 28377 que obliga a editores, impresores, autores , productores e importadores, a entregar a la Biblioteca Nacional del Perú, un determinado número de ejemplares de las obras editadas, producidas y/o distribuidas en el Perú. El trámite es gratuito.</p>

                <strong>¿Cómo realizo el Depósito Legal?</strong>

                <ul class="arrow-list">
                    <li>Paso 1: Reservar el número de Depósito Legal vía formulario eléctronico en www.bnp.gob.pe o personalmente en la Biblioteca Nacional del Perú</li>
                    <li>Paso 2: Consignar en lugar visible de la obra la frase "Hecho el Depósito Legal en la Biblioteca Nacional del Perú", seguido por el número asignado, además de su razón social y domicilio legal.</li>
                    <li>Paso 3: Entregar a la Biblioteca Nacional del Perú, los ejemplares que corresponda según el tipo de publicación. En el caso de libros, uno de éstos ejemplares se deriva a la Biblioteca de la Municipalidad Provincial, donde se imprimió la obra.</li>
                </ul>




                <strong>ISBN</strong>

                <p>El ISBN es un sistema internacional de numeración de libros, aprobado como norma ISO 2108. Este númeroidentifica individualmente a cada título a nivel mundial, permitiéndo una mejor comercialización y distribución de los mismos.

                    El ISBN puede ser solcitado por los editores y autores-editores, mas no por los impresores ni distribuidores

                    La Agencia Peruana del ISB, ubicada en la Biblioteca Nacional del Perú, es registradora local de la Agencia Internacional del ISBN</p>


                <strong>Requisitos</strong>


                <ul class="arrow-list">
                    <li>LLenar la ficha del Editor, la cual debe estar firmada y sellada.</li>
                    <li>Adjuntar copia de RUC.</li>
                    <li>Llenar la ficha de Registro de Título, firmada y sellada.</li>
                    <li>Presentar el machote e indice de libro a publicar.</li>
                    <li>Adjuntar copia de recibo de pago.</li>
                    <li>Enviar un ejemplar publicado para verificación y actualización de datos.   </li>
                </ul>                            



